  <p class="content-desc text-justify">
    Sipinrang merupakan sebuah sistem informasi yang dibuat untuk mempermudah proses peminjaman ruang yang ada di
    Polstat STIS. <br><br>
    Sipinrang dikembangkan dengan alasan untuk mempermudah mekanisme yang sudah berjalan hingga saat ini, yaitu melalui
    perantara BEM yang kemudian diajukan kepada BAU, dan diteruskan ke satpam serta CS. Mekanisme tersebut memiliki
    keterbatasan karena yang hendak meminjam ruangan tidak dapat mengetahui apakah tersedia atau tidak. <br><br>
    Dengan dikembangkannya Sipinrang ini diharapkan dapat mengatasi keterbatasan-keterbatasan mekanisme yang sudah
    berjalan hingga saat ini, serta proses peminjaman ruang di Polstat STIS menjadi lebih efisien dan efektif.
  </p>
